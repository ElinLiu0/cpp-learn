#include <iostream>
using namespace std;

int main(void){
    int decimal = 42; // 以十进制方式进行表达
    int oct = 042; // 以八进制进行表达
    int hex = 0x42; // 以十六进制进行表达
    cout << "decimal 42 = " << decimal << endl;
    cout << "octal 42 = " << oct << endl;
    cout << "hexadecimal 42 = " << hex << endl;
    return 0;
}